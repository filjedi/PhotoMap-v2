- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    NSLog(@"[_annotations count] = %li", (long)[_annotations count]);
    return [_annotations count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"AnnotationCell" forIndexPath:indexPath];
    
    // Configure the cell...
    RGMMapViewAnnotation *annotation = _annotations[indexPath.row];
    if ([annotation isKindOfClass:[MKUserLocation class]]) {
        cell.imageView.image = nil;
    }
    else {
        cell.imageView.image = annotation.annotationImage;
    }
    cell.textLabel.text = annotation.title;
    cell.detailTextLabel.text = [[NSString alloc] initWithFormat:@"image lat %f lon %f", annotation.coordinate.latitude, annotation.coordinate.longitude];
    
    return cell;
}


